function updateProgress() {
    const total = numbersList.length;
    const checked = numbersList.filter(n => {
        const cb = document.getElementById('check-' + n);
        return cb && cb.checked;
    }).length;

    const progressLabel = document.getElementById('progress-label');
    const progressBarFill = document.getElementById('progress-bar-fill');

    if (progressLabel) {
        progressLabel.innerText = `${checked} / ${total} ${t().progressChecked}`;
    }

    if (progressBarFill) {
        progressBarFill.style.width = `${total ? (checked / total) * 100 : 0}%`;
        progressBarFill.style.background =
            checked === total && total > 0
                ? 'linear-gradient(90deg, #f5c400, #ffe066)'
                : 'linear-gradient(90deg, #35D49F, #05EB20)';
    }
}

function vibrate() {
    if ('vibrate' in navigator) navigator.vibrate(50);
}

function playSuccessSound() {
    const audioCtx = new (window.AudioContext || window.webkitAudioContext)();
    const oscillator = audioCtx.createOscillator();
    const gainNode = audioCtx.createGain();

    oscillator.connect(gainNode);
    gainNode.connect(audioCtx.destination);

    oscillator.type = 'sine';
    oscillator.frequency.setValueAtTime(880, audioCtx.currentTime);
    oscillator.frequency.setValueAtTime(1100, audioCtx.currentTime + 0.1);
    gainNode.gain.setValueAtTime(0.4, audioCtx.currentTime);
    gainNode.gain.exponentialRampToValueAtTime(0.001, audioCtx.currentTime + 0.4);

    oscillator.start(audioCtx.currentTime);
    oscillator.stop(audioCtx.currentTime + 0.4);
}

function playUnrecognizedSound() {
    const audioCtx = new (window.AudioContext || window.webkitAudioContext)();
    const oscillator = audioCtx.createOscillator();
    const gainNode = audioCtx.createGain();

    oscillator.connect(gainNode);
    gainNode.connect(audioCtx.destination);

    oscillator.type = 'sawtooth';
    oscillator.frequency.setValueAtTime(300, audioCtx.currentTime);
    oscillator.frequency.setValueAtTime(200, audioCtx.currentTime + 0.15);
    gainNode.gain.setValueAtTime(0.3, audioCtx.currentTime);
    gainNode.gain.exponentialRampToValueAtTime(0.001, audioCtx.currentTime + 0.4);

    oscillator.start(audioCtx.currentTime);
    oscillator.stop(audioCtx.currentTime + 0.4);
}

function renderTable() {
    const tbody = document.getElementById('number-table-body');
    if (!tbody) return;

    tbody.innerHTML = '';

    numbersList.forEach(number => {
        const tr = document.createElement('tr');
        tr.dataset.number = String(number);

        const tdNumber = document.createElement('td');
        tdNumber.textContent = number;

        const tdCheck = document.createElement('td');
        const checkbox = document.createElement('input');
        checkbox.type = 'checkbox';
        checkbox.id = `check-${number}`;

        checkbox.addEventListener('change', () => {
            tr.classList.toggle('checked-row', checkbox.checked);
            updateProgress();
        });

        tdCheck.appendChild(checkbox);
        tr.appendChild(tdNumber);
        tr.appendChild(tdCheck);
        tbody.appendChild(tr);
    });

    updateProgress();
}

function parseKeyText(text) {
    const tokens = text.split(/[\s,;]+/).map(t => t.trim()).filter(Boolean);
    const parsed = tokens
        .map(t => parseInt(t, 10))
        .filter(n => !isNaN(n));

    return [...new Set(parsed)].sort((a, b) => a - b);
}

document.getElementById('key-file')?.addEventListener('change', event => {
    const file = event.target.files[0];
    if (!file) return;

    const reader = new FileReader();
    reader.onload = e => {
        try {
            const text = String(e.target.result || '');
            const parsedNumbers = parseKeyText(text);

            if (!parsedNumbers.length) {
                alert(t().keyNoValidNumbers);
                return;
            }

            numbersList = parsedNumbers;
            renderTable();
            alert(t().keyLoaded(numbersList.length));
        } catch {
            alert(t().keyReadError);
        }
    };

    reader.readAsText(file);
    event.target.value = '';
});

function ensureRecognition() {
    if (recognition) return recognition;
    if (!(window.SpeechRecognition || window.webkitSpeechRecognition)) return null;

    recognition = new (window.SpeechRecognition || window.webkitSpeechRecognition)();
    recognition.continuous = true;
    recognition.interimResults = false;
    recognition.lang = t().speechLang;

    recognition.onresult = event => {
        const result = event.results[event.results.length - 1][0].transcript;
        const output = document.getElementById('output');
        if (output) output.innerText = result;
        markNumberAsChecked(result);
    };

    recognition.onend = () => {
        if (shouldRestartRecognition && isListening) {
            setTimeout(() => {
                try {
                    recognition.lang = t().speechLang;
                    recognition.start();
                } catch {}
            }, 150);
        }
    };

    recognition.onerror = () => {};

    return recognition;
}

async function toggleRecognition() {
    if (!numbersList.length) {
        alert(t().loadKeyFirst);
        return;
    }

    const instance = ensureRecognition();
    if (!instance) {
        alert(t().speechNotSupported);
        return;
    }

    if (!isListening) {
        try {
            instance.lang = t().speechLang;
            shouldRestartRecognition = true;
            instance.start();
            isListening = true;
        } catch {}
    } else {
        shouldRestartRecognition = false;
        isListening = false;
        try {
            instance.stop();
        } catch {}
    }

    applyLanguage();
}

function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'de' : 'en';
    applyLanguage();

    if (recognition) {
        recognition.lang = t().speechLang;
    }

    if (isListening && recognition) {
        shouldRestartRecognition = false;
        try {
            recognition.stop();
        } catch {}

        setTimeout(() => {
            if (isListening && recognition) {
                try {
                    recognition.lang = t().speechLang;
                    shouldRestartRecognition = true;
                    recognition.start();
                } catch {}
            }
        }, 200);
    }
}

function markNumberAsChecked(text) {
    const normalizedText = normalizeTranscript(text);
    const numbers = normalizedText.match(/\d+/g);
    let matched = false;

    if (numbers) {
        for (const numberStr of numbers) {
            const number = parseInt(numberStr, 10);
            if (!isNaN(number) && numbersList.includes(number)) {
                const checkbox = document.getElementById('check-' + number);
                if (checkbox && !checkbox.checked) {
                    checkbox.checked = true;
                    checkbox.closest('tr')?.classList.add('checked-row');
                    playSuccessSound();
                    vibrate();
                    checkbox.closest('tr')?.scrollIntoView({ behavior: 'smooth', block: 'center' });
                    updateProgress();
                    matched = true;
                }
            }
        }
    }

    if (!matched) playUnrecognizedSound();
}

