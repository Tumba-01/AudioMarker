function normalizeEntry(entry) {
    if (typeof entry === 'object' && entry !== null) {
        return {
            number: Number(entry.number),
            ort: String(entry.ort || ''),
            checked: Boolean(entry.checked),
            comment: String(entry.comment || '')
        };
    }

    return {
        number: Number(entry),
        ort: '',
        checked: false,
        comment: ''
    };
}

function getEntries() {
    return numbersList
        .map(normalizeEntry)
        .filter(entry => !isNaN(entry.number))
        .sort((a, b) => a.number - b.number);
}

function setEntries(entries) {
    numbersList = entries
        .map(entry => ({
            number: Number(entry.number),
            ort: String(entry.ort || ''),
            checked: Boolean(entry.checked),
            comment: String(entry.comment || '')
        }))
        .filter(entry => !isNaN(entry.number))
        .sort((a, b) => a.number - b.number);
}

function updateProgress() {
    const entries = getEntries();
    const total = entries.length;
    const checked = entries.filter(entry => {
        const cb = document.getElementById('check-' + entry.number);
        return cb && cb.checked;
    }).length;

    const progressLabel = document.getElementById('progress-label');
    const progressBarFill = document.getElementById('progress-bar-fill');

    if (progressLabel) {
        progressLabel.innerText = `${checked} / ${total} ${t().progressChecked}`;
    }

    if (progressBarFill) {
        progressBarFill.style.width = `${total ? (checked / total) * 100 : 0}%`;
        progressBarFill.style.background =
            checked === total && total > 0
                ? 'linear-gradient(90deg, #f5c400, #ffe066)'
                : 'linear-gradient(90deg, #35D49F, #05EB20)';
    }
}

