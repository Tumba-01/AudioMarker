function buildSessionData(comments = {}) {
    const now = new Date();
    const checked = [];
    const unchecked = [];

    for (const number of numbersList) {
        const cb = document.getElementById('check-' + number);
        if (cb && cb.checked) {
            checked.push(number);
        } else {
            unchecked.push({
                number,
                reason: comments[number] || t().defaultUncheckedReason
            });
        }
    }

    return {
        language: currentLanguage,
        exportedAt: now.toISOString(),
        exportedAtReadable: now.toLocaleString(t().locale),
        complete: unchecked.length === 0,
        numbers: [...numbersList],
        checked,
        unchecked
    };
}

function doExportJSON(comments = {}) {
    const data = buildSessionData(comments);
    const blob = new Blob([JSON.stringify(data, null, 2)], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    const dateStr = new Date().toISOString().slice(0, 10);

    a.href = url;
    a.download = `${t().exportFileName(dateStr)}.json`;
    a.click();

    URL.revokeObjectURL(url);
}

function doExportPDF(comments = {}) {
    const data = buildSessionData(comments);
    const lang = translations[data.language] || t();
    const { jsPDF } = window.jspdf;

    const doc = new jsPDF({
        orientation: 'portrait',
        unit: 'mm',
        format: 'a4'
    });

    const pageHeight = doc.internal.pageSize.height;
    const pageWidth = doc.internal.pageSize.width;
    const marginBottom = 15;
    const marginTop = 20;

    function addHeader(yPosition) {
        doc.setFont('helvetica', 'bold');
        doc.setFontSize(10);
        doc.text(lang.pdfHeaderNumber, 25, yPosition);
        doc.text(lang.pdfHeaderStatus, 70, yPosition);
        doc.text(lang.pdfHeaderComment, 120, yPosition);
        doc.setLineWidth(0.5);
        doc.line(20, yPosition + 2, 190, yPosition + 2);
        doc.setFont('helvetica', 'normal');
        return yPosition + 8;
    }

    doc.setFontSize(16);
    doc.setFont('helvetica', 'bold');
    doc.text(lang.pdfTitle, 105, 20, { align: 'center' });

    doc.setFontSize(12);
    doc.setFont('helvetica', 'normal');
    doc.text(lang.pdfSubtitle, 105, 28, { align: 'center' });
    doc.text(lang.pdfDate(data.exportedAtReadable), 105, 35, { align: 'center' });

    doc.setFontSize(10);
    doc.text(lang.pdfIntro, 20, 45);

    let yPos = addHeader(55);

    for (const number of data.checked) {
        if (yPos + 7 > pageHeight - marginBottom) {
            doc.addPage();
            yPos = addHeader(marginTop);
        }
        doc.text(String(number), 25, yPos);
        doc.text(`✓ ${lang.pdfTriggered}`, 70, yPos);
        doc.text('—', 120, yPos);
        yPos += 7;
    }

    for (const item of data.unchecked) {
        const comment = item.reason || lang.noComment;
        const lines = doc.splitTextToSize(comment, 65);
        const lineHeight = lines.length * 5;
        const rowHeight = Math.max(7, lineHeight);

        if (yPos + rowHeight > pageHeight - marginBottom) {
            doc.addPage();
            yPos = addHeader(marginTop);
        }

        doc.text(String(item.number), 25, yPos);
        doc.text(`✗ ${lang.pdfNotTriggered}`, 70, yPos);
        doc.text(lines, 120, yPos);
        yPos += rowHeight;
    }

    const totalPages = doc.internal.getNumberOfPages();

    for (let i = 1; i <= totalPages; i++) {
        doc.setPage(i);
        doc.setFontSize(8);
        doc.setFont('helvetica', 'italic');
        doc.text(lang.pdfCreatedAt(data.exportedAtReadable), 20, pageHeight - 10);
        doc.text(lang.pdfPage(i, totalPages), pageWidth - 20, pageHeight - 10, { align: 'right' });
    }

    const dateStr = new Date().toISOString().slice(0, 10);
    doc.save(`${lang.exportFileName(dateStr)}.pdf`);
}

