function t() {
    return translations[currentLanguage];
}

function normalizeTranscript(text) {
    return text
        .toLowerCase()
        .split(/\s+/)
        .map(word => wordToNumber[word] !== undefined ? wordToNumber[word] : word)
        .join(' ');
}

