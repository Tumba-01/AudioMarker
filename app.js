document.getElementById('random-check')?.addEventListener('click', () => {
    const entries = getEntries();

    const uncheckedEntries = entries.filter(entry => {
        const checkbox = document.getElementById('check-' + entry.number);
        return !(checkbox && checkbox.checked);
    });

    if (!uncheckedEntries.length) {
        alert(t().allNumbersChecked);
        return;
    }

    const shuffled = [...uncheckedEntries].sort(() => Math.random() - 0.5);
    const targetCount = Math.max(1, Math.ceil(uncheckedEntries.length / 2));
    const toCheck = shuffled.slice(0, targetCount);

    toCheck.forEach(entry => {
        const checkbox = document.getElementById('check-' + entry.number);
        if (checkbox && !checkbox.checked) {
            checkbox.checked = true;
            checkbox.closest('tr')?.classList.add('checked-row');
        }

        const found = numbersList.find(item => typeof item === 'object' && item.number === entry.number);
        if (found) {
            found.checked = true;
        }
    });

    updateProgress();
});


