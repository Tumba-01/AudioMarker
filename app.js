function shuffleArray(array) {
    const arr = [...array];
    for (let i = arr.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    return arr;
}

function randomCheckHalf() {
    if (!numbersList.length) {
        alert(t().loadKeyFirst);
        return;
    }

    const remaining = numbersList.filter(number => {
        const checkbox = document.getElementById('check-' + number);
        return checkbox && !checkbox.checked;
    });

    if (!remaining.length) {
        alert(t().allNumbersChecked);
        return;
    }

    const amountToAdd = Math.ceil(remaining.length / 2);
    const selected = shuffleArray(remaining).slice(0, amountToAdd);

    for (const number of selected) {
        const checkbox = document.getElementById('check-' + number);
        if (checkbox) {
            checkbox.checked = true;
            checkbox.closest('tr')?.classList.add('checked-row');
        }
    }

    updateProgress();
}

document.getElementById('random-check')?.addEventListener('click', randomCheckHalf);

applyLanguage();
updateProgress();
