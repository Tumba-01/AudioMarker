function ensureRecognition() {
    if (recognition) return recognition;
    if (!(window.SpeechRecognition || window.webkitSpeechRecognition)) return null;

    recognition = new (window.SpeechRecognition || window.webkitSpeechRecognition)();
    recognition.continuous = true;
    recognition.interimResults = false;
    recognition.lang = t().speechLang;

    recognition.onresult = event => {
        const result = event.results[event.results.length - 1][0].transcript;
        const output = document.getElementById('output');
        if (output) output.innerText = result;
        markNumberAsChecked(result);
    };

    recognition.onend = () => {
        if (shouldRestartRecognition && isListening) {
            setTimeout(() => {
                try {
                    recognition.lang = t().speechLang;
                    recognition.start();
                } catch {}
            }, 150);
        }
    };

    recognition.onerror = () => {};

    return recognition;
}

async function toggleRecognition() {
    if (!numbersList.length) {
        alert(t().loadKeyFirst);
        return;
    }

    const instance = ensureRecognition();
    if (!instance) {
        alert(t().speechNotSupported);
        return;
    }

    if (!isListening) {
        try {
            instance.lang = t().speechLang;
            shouldRestartRecognition = true;
            instance.start();
            isListening = true;
        } catch {}
    } else {
        shouldRestartRecognition = false;
        isListening = false;
        try {
            instance.stop();
        } catch {}
    }

    applyLanguage();
}

function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'de' : 'en';
    applyLanguage();

    if (recognition) {
        recognition.lang = t().speechLang;
    }

    if (isListening && recognition) {
        shouldRestartRecognition = false;
        try {
            recognition.stop();
        } catch {}

        setTimeout(() => {
            if (isListening && recognition) {
                try {
                    recognition.lang = t().speechLang;
                    shouldRestartRecognition = true;
                    recognition.start();
                } catch {}
            }
        }, 200);
    }
}

function markNumberAsChecked(text) {
    const normalizedText = normalizeTranscript(text);
    const numbers = normalizedText.match(/\d+/g);
    let matched = false;

    if (numbers) {
        for (const numberStr of numbers) {
            const number = parseInt(numberStr, 10);
            const exists = getEntries().some(entry => entry.number === number);

            if (!isNaN(number) && exists) {
                const checkbox = document.getElementById('check-' + number);
                if (checkbox && !checkbox.checked) {
                    checkbox.checked = true;
                    checkbox.closest('tr')?.classList.add('checked-row');
                    const found = numbersList.find(item => typeof item === 'object' && item.number === number);
                    if (found) found.checked = true;
                    playSuccessSound();
                    vibrate();
                    checkbox.closest('tr')?.scrollIntoView({ behavior: 'smooth', block: 'center' });
                    updateProgress();
                    matched = true;
                }
            }
        }
    }

    if (!matched) playUnrecognizedSound();
}

